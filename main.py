def admin_only(function):
    @wraps(function)
    def wrapper_function(*args, **kwargs):
        if current_user.id == 1:
            function()
        else:
            return abort(403)
        return function(*args, **kwargs)
    return wrapper_function


@app.route('/register', methods=["POST", "GET"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        new_user = User(
            name=form.name.data,
            email=form.email.data,
            password=generate_password_hash(form.password.data, method="pbkdf2:sha256", salt_length=8)
        )
        result = db.session.execute(db.select(User).where(User.email == new_user.email))
        is_registered = result.scalar()
        if is_registered:
            flash('User already exists')
            print('User already exists')
            return redirect(url_for("login"))
        else:
            db.session.add(new_user)
            db.session.commit()
            # Log in and authenticate user after adding details to database.
            flask.flash('Logged in successfully.')
            print('Logged in successfully.')
            login_user(new_user)
            return render_template("index.html", name=new_user.name)
    return render_template("register.html", form=form)


@app.route('/login', methods=["POST", "GET"])
def login():
    form = LoginForm()
    user_admin = False
    if request.method == "POST":
        email = request.form.get("email")
        password = request.form.get("password")
        user = User.query.filter_by(email=email).first()
        if user:
            if check_password_hash(user.password, password):
                flash('Logged in successfully.')
                login_user(user)
                if user.id == 1:
                    user_admin = True
                    return render_template("index.html", name=user.name, user=user_admin)
            else:
                flash('Wrong password for this User')
                return render_template("login.html", form=form)
        else:
            flash('No matching User-Please register')
        return redirect(url_for("register"))
    return render_template("login.html", form=form)


